"""
1. Equal (==):

Checks if two values are equal.
Example: x == y returns True if x is equal to y.

2.Not Equal (!=):
Checks if two values are not equal.
Example: x != y returns True if x is not equal to y.

3.Greater Than (>):
Checks if the left-hand side value is greater than the right-hand side value.
Example: x > y returns True if x is greater than y.

4.Less Than (<):
Checks if the left-hand side value is less than the right-hand side value.
Example: x < y returns True if x is less than y.

5.Greater Than or Equal To (>=):
Checks if the left-hand side value is greater than or equal to the right-hand side value.
Example: x >= y returns True if x is greater than or equal to y.

6.Less Than or Equal To (<=):
Checks if the left-hand side value is less than or equal to the right-hand side value.
Example: x <= y returns True if x is less than or equal to y.
These comparison operators are commonly used in conditional statements (e.g., if, elif, else) to make decisions based on the comparison results. For example:

"""

#******** EXAMPLE 01 ********

x = 5
y = 10

if x > y:
    print("x is greater than y")
elif x < y:
    print("x is less than y")
else:
    print("x and y are equal")
