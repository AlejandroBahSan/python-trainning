"""
In Python, a lambda function is a small, anonymous function that is defined using the lambda keyword. 
Lambda functions are also known as "anonymous functions" because they don't have a name. 
They are typically used for short, simple operations where you don't want to define a full function using the def keyword.
"""

# functions example (lambda) - 05
"""
Lambda version:

add = lambda x, y: x + y


result = add(3, 5)
print(result)  # This will print 8
"""


def add(x, y): return x + y


result = add(3, 5)
print(result)  # This will print 8
